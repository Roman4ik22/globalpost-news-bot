def get_article_prompt(format_type: str, source_url: str) -> str:
    """Получить промпт для генерации поста нужного формата."""

    formats = {
        "news": _format_news(source_url),
        "stat": _format_stat(source_url),
        "analysis": _format_analysis(source_url),
    }
    return formats.get(format_type, formats["news"])


